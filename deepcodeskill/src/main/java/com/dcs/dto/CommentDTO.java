package com.dcs.dto;

public class CommentDTO {

	private String comment;

	public CommentDTO(String comment) {
		super();
		this.comment = comment;
	}
	
	public CommentDTO() {
		
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}
	
	
}
